#define MAXN 2200
int dp[MAXN][MAXN], memo[MAXN][MAXN], n;
char s[MAXN];

int calc(int l, int r) {
	if (l > r) {
		return 0;
	}
	if (l == r) {
		return 1;
	}
	if (memo[l][r]) {
		return dp[l][r];
	}
	memo[l][r] = 1;
	if (s[l] == s[r]) {
		dp[l][r] = calc(l+1, r-1) + 1;
	}
	else {
		dp[l][r] = max(calc(l+1, r), calc(l, r-1));
	}
	return dp[l][r];
	
}

int main(){
	scanf("%s", s);
	n = strlen(s);
	printf("%d\n", calc(0, n-1));
	
	return 0;
}
