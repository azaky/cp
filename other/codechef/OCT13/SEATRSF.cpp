#define MOD 1000000007LL

ll p(ll a, ll b){
	if (b == 0) return 1LL;
	ll ret = p(a,b/2LL);
	ret = (ret * ret) % MOD;
	if (b%2LL) ret = (ret * a) % MOD;
	return ret;
}

int main(){
	ll ntc, n, m, q, k;
	cin >> ntc;
	while (ntc--){
		cin >> n >> m >> q >> k;
		q++;
		if (m < q || n == 1) cout << "0\n";
		else cout << ((m-q+1) * (p(q,n) + p(q-2,n) - 2LL*p(q-1,n) + 2*MOD) % MOD) % MOD << endl;
	}
	return 0;
}
