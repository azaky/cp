#define MAXN 10100
char dna[MAXN],swine[MAXN];
int n, m, ans, cost[256];

int main(){
	scanf("%s %s %d %d %d %d",dna,swine,&cost['A'],&cost['C'],&cost['G'],&cost['T']);
	
	ans = 2000000000;
	
	n = strlen(dna);
	m = strlen(swine);
	
	REP(i,n) {
		int k = i;
		int total = 0;
		REP(j,m) {
			if (dna[k] == swine[j] && k < n) k++;
			else total += cost[swine[j]];
		}
		MN(ans,total);
	}
	
	printf("%d\n",ans);
	
	return 0;
}
