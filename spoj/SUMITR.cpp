int t,n,i,j,x,d[100],a;

int main(){
	scanf("%d",&t);
	while(t--){
		scanf("%d",&n);
		a=d[n]=0;
		for(i=n;i;--i){
			for(j=i;j<=n;++j){
				scanf("%d",&x);
				d[j]=x+(j==i?d[j+1]:max(d[j],d[j+1]));
				a=max(a,d[j]);
			}
		}
		printf("%d\n",a);
	}
		
	
	return 0;
}
