int main(){
	int n, _n;
	scanf("%d",&n); _n = n;
	if (n == 0) return 0;
	int ans = 1;
	int p = 2;
	int lim = 1;
	while (lim*lim < n) lim++;
	while (n != 1 && p <= lim){
		int k = 0;
		while (n % p == 0){
			n /= p;
			k ++;
		}
		ans *= 2*k + 1;
		p++;
	}
	if (n != 1) ans *= 3;
	ans = (ans+1)/2;
	printf("%d %d\n",_n,ans);
	
	main();
	return 0;
}
